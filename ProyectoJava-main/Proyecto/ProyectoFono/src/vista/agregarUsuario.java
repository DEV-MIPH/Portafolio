    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        int rut;
        String dv,pnombre,snombre,apaterno,amaterno,usuario,password,correo,rol;
        validacion val = new validacion();
        
        try{
            rut = Integer.parseInt(this.txtRut.getText());
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "rut ingresado no es valido","Validacion",JOptionPane.WARNING_MESSAGE);
            this.txtRut.requestFocus();
            return;
        }
        dv = this.txtDV.getText();
        dv.toUpperCase();
        if(dv.isEmpty()){
            JOptionPane.showMessageDialog(this, "Porfavor Ingresar dv","Validacion",JOptionPane.WARNING_MESSAGE);
            this.txtDV.requestFocus();
            return;
        }
        String rutCompleto = rut+"-"+dv;
        if(!val.validarRut(rutCompleto)){
            JOptionPane.showMessageDialog(this, "rut ingresado no es valido","Validacion",JOptionPane.WARNING_MESSAGE);
            this.txtDV.requestFocus();
            this.txtRut.requestFocus();
            return;
        }
        pnombre = this.txtPnombre.getText();
        if(pnombre.isEmpty()){
            JOptionPane.showMessageDialog(this, "Porfavor Ingresar primer nombre","Validacion",JOptionPane.WARNING_MESSAGE);
            this.txtPnombre.requestFocus();
            return;
        }
        snombre = this.txtSnombre.getText();

        apaterno = this.txtApaterno.getText();
        if(apaterno.isEmpty()){
            JOptionPane.showMessageDialog(this, "Porfavor Ingresar apellido paterno","Validacion",JOptionPane.WARNING_MESSAGE);
            this.txtRol.requestFocus();
            return;
        }
        amaterno = this.txtAmaterno.getText();
        if(apaterno.isEmpty()){
            JOptionPane.showMessageDialog(this, "Porfavor Ingresar apellido materno","Validacion",JOptionPane.WARNING_MESSAGE);
            this.txtAmaterno.requestFocus();
            return;
        }
        password = String.valueOf(txtPass1.getPassword());
        if(password.isEmpty()||String.valueOf(txtPass2.getPassword()).isEmpty()){
            JOptionPane.showMessageDialog(this, "Las Contrase単as no coinciden o estan vacias","Validacion",JOptionPane.WARNING_MESSAGE);
            this.txtPass1.requestFocus();
            this.txtPass2.requestFocus();
            return;
        }
        if(password.equals(String.valueOf(txtPass2.getPassword())) == false){
            JOptionPane.showMessageDialog(this, "Las Contrase単as no coinciden","Validacion",JOptionPane.WARNING_MESSAGE);
            this.txtPass1.requestFocus();
            this.txtPass1.setText("");
            this.txtPass2.setText("");
            return; 
            
        }
        
        correo = this.txtCorreo.getText();
       
        boolean resultado = val.validarCorreo(correo);
        if (!resultado){
            JOptionPane.showMessageDialog(this, "Porfavor Ingresar correo valido","Validacion",JOptionPane.WARNING_MESSAGE);
            this.txtCorreo.requestFocus();
            return;
        }
        
        if(correo.isEmpty()){
            JOptionPane.showMessageDialog(this, "Porfavor Ingresar correo","Validacion",JOptionPane.WARNING_MESSAGE);
            this.txtCorreo.requestFocus();
            return;
        }
        rol = this.txtRol.getText();
        if(rol.isEmpty()){
            JOptionPane.showMessageDialog(this, "Porfavor Ingresar rol","Validacion",JOptionPane.WARNING_MESSAGE);
            this.txtRol.requestFocus();
            return;
        }
        usuario = this.txtUsuario.getText();
        if(usuario.isEmpty()){
            JOptionPane.showMessageDialog(this, "Porfavor Ingresar rol","Validacion",JOptionPane.WARNING_MESSAGE);
            this.txtRol.requestFocus();
            return;
        }
        
        
        
        
        trabajador trabajador = new trabajador(usuario, password, correo, rol, rut, dv, pnombre, snombre,  apaterno, amaterno);
        conexionUsuario user = new conexionUsuario();
        
        
        
        
        
        if(user.registrarTrabajador(trabajador)){
            JOptionPane.showMessageDialog(this, "Trabajador agregado con exito","Felicidades",JOptionPane.INFORMATION_MESSAGE);            
            this.txtRut.setText("");
            this.txtDV.setText("");
            this.txtRut.setText("");
            this.txtSnombre.setText("");
            this.txtRol.setText("");
            this.txtAmaterno.setText("");
            this.txtPass1.setText("");
            this.txtPass2.setText("");
            this.txtUsuario.setText("");
            this.txtRol.setText("");
            this.txtPnombre.setText("");
            this.txtCorreo.setText("");
            this.txtApaterno.setText("");

        }else{
            JOptionPane.showMessageDialog(this, "Error no se ha podido agregar el usuario","Error",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

