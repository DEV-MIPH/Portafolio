    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String rut;
        String dv,pnombre,snombre,apaterno,amaterno;
        int rutNumber = 0;
        validacion val = new validacion();
        String rutNew = "";
        
        conexionUsuario Usuario = new conexionUsuario();
        DefaultTableModel modelo = (DefaultTableModel)this.tblBusqueda.getModel();
        try{
            
            rut = this.txtRut.getText();
            
                rutNew = rut.substring(0,rut.length()-2);
                System.out.println(rutNew);

            
            try {
                rutNumber = Integer.valueOf(rutNew);
            }
            catch (NumberFormatException ex){
                ex.printStackTrace();     
            }
        }catch(Exception e){
            rut = "";
        }
        modelo.setRowCount(0);
        String eleccion = this.cbBusqueda.getSelectedItem() + "";
        
        if(eleccion.equals("Paciente")){
                List<paciente> lista;
                if(rut.equals("")){
                    lista = Usuario.buscarTodosLosPaciente();
                }else{
                    lista = Usuario.buscarPorRutPaciente(rutNumber);
                }
                for(paciente paciente:lista){
                    String rutFinal = paciente.getRut() + "-"+paciente.getDv();
                    String NombreFinal = paciente.getPnombre()+" "+paciente.getSnombre()+" "+
                            paciente.getApaterno()+" "+paciente.getAmaterno();
                    modelo.addRow(new Object[]{rutFinal,NombreFinal});
                }
                if(lista.isEmpty()){
                    JOptionPane.showMessageDialog(this, "No se ha encontrado el paciente","Validacion",JOptionPane.WARNING_MESSAGE);
                }
            }
        else {            
                List<trabajador> lista;
                if(rut.equals("")){
                    lista = Usuario.TodosLosTrabajadores();
                }else{
                    lista = Usuario.buscarXtrabajador(rutNumber);
                }
                for(trabajador trabajador:lista){
                        String rutFinal = trabajador.getRut()+ "-"+trabajador.getDv();  
                        String NombreFinal = trabajador.getPnombre()+" "+trabajador.getSnombre()+" "+
                                trabajador.getApaterno()+" "+trabajador.getAmaterno();
                        modelo.addRow(new Object[]{rutFinal,NombreFinal});
                
                }
                if(lista.isEmpty()){
                    JOptionPane.showMessageDialog(this, "No se ha encontrado trabajador","Validacion",JOptionPane.WARNING_MESSAGE);
            }
        }
        this.txtRut.setText("");
                
        
    }//GEN-LAST:event_btnBuscarActionPerformed

